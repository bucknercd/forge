def _artifact_rel(path: Path) -> str:
    try:
        return path.resolve().relative_to(Paths.BASE_DIR.resolve()).as_posix()
    except ValueError:
        return str(path)


def materialize_bundle(
    bundle: VerticalSliceBundle,
    *,
    event_bus: object | None = None,
    failure_artifact_dir: Path | None = None,
) -> None:
    bus = as_emitter(event_bus)
    Paths.ensure_project_structure()
    # Parse before any write so we never persist broken milestones.md from a bad bundle.
    try:
        MilestoneService.parse_milestones(bundle.milestones_md)
    except ValueError as exc:
        if failure_artifact_dir is not None:
            failure_artifact_dir.mkdir(parents=True, exist_ok=True)
            (
                failure_artifact_dir / "milestones_md_materialize_parse_failure.txt"
            ).write_text(bundle.milestones_md, encoding="utf-8")
        raise ValueError(
            f"Refusing to write docs/milestones.md: milestone markdown is invalid ({exc}). "
            "Fix milestones_md or re-run vertical-slice / LLM generation."
        ) from exc

    VisionManager.save_vision(bundle.vision)
    bus.emit(ARTIFACT_WRITTEN, path=_artifact_rel(Paths.VISION_FILE), kind="vision")
    DesignManager.save_document(Paths.REQUIREMENTS_FILE, bundle.requirements_md)
    bus.emit(
        ARTIFACT_WRITTEN, path=_artifact_rel(Paths.REQUIREMENTS_FILE), kind="requirements"
    )
    DesignManager.save_document(Paths.ARCHITECTURE_FILE, bundle.architecture_md)
    bus.emit(
        ARTIFACT_WRITTEN,
        path=_artifact_rel(Paths.ARCHITECTURE_FILE),
        kind="architecture",
    )
    DesignManager.save_document(Paths.MILESTONES_FILE, bundle.milestones_md)
    bus.emit(
        ARTIFACT_WRITTEN, path=_artifact_rel(Paths.MILESTONES_FILE), kind="milestones"
    )
    # Re-read from disk for parity with the rest of the pipeline
    MilestoneService.list_milestones()


def _last_nonempty_osv_line(rest: list[str], prefix: str) -> str:
    """Last non-empty value for a field, matching MilestoneService last-wins overwrite behavior."""
    last_nonempty = ""
    for line in rest:
        st = line.strip()
        if st.startswith(prefix):
            tail = line.split(":", 1)[1].strip() if ":" in line else ""
            if tail:
                last_nonempty = tail
    return last_nonempty


def _strip_osv_lines_from_rest(rest: list[str]) -> list[str]:
    out: list[str] = []
    for line in rest:
        st = line.strip()
        if st.startswith(
            (
                "- **Objective**:",
                "- **Scope**:",
                "- **Validation**:",
            )
        ):
            continue
        out.append(line)
    return out


def _repair_single_milestone_section(
    section: list[str], milestone_id: str
) -> tuple[list[str], list[str]]:
    warnings: list[str] = []
    depends: list[str] = []
    i = 0
    while i < len(section) and section[i].strip().startswith("- **Depends On**:"):
        depends.append(section[i])
        i += 1
    rest = section[i:]
    obj = _last_nonempty_osv_line(rest, "- **Objective**:")
    scope = _last_nonempty_osv_line(rest, "- **Scope**:")
    val = _last_nonempty_osv_line(rest, "- **Validation**:")
    other = _strip_osv_lines_from_rest(rest)
    body: list[str] = []
    body.extend(depends)
    if not obj.strip():
        obj = _PLACEHOLDER_OBJECTIVE
        warnings.append(
            f"Milestone {milestone_id}: inserted placeholder Objective (missing or empty)."
        )
    if not scope.strip():
        scope = _PLACEHOLDER_SCOPE
        warnings.append(
            f"Milestone {milestone_id}: inserted placeholder Scope (missing or empty)."
        )
    if not val.strip():
        val = _PLACEHOLDER_VALIDATION
        warnings.append(
            f"Milestone {milestone_id}: inserted placeholder Validation (missing or empty)."
        )
    body.append(f"- **Objective**: {obj}")
    body.append(f"- **Scope**: {scope}")
    body.append(f"- **Validation**: {val}")
    body.extend(other)
    return body, warnings


def repair_llm_milestones_md(md: str) -> tuple[str, list[str]]:
    """
    Ensure each ``## Milestone N:`` section has non-empty Objective, Scope, and Validation.
    Inserts minimal placeholder text when the LLM omitted or emptied a field.
    Returns ``(repaired_markdown, warning_messages)``.
    """
    if not (md or "").strip():
        return md, []
    lines = md.splitlines()
    out: list[str] = []
    warnings: list[str] = []
    i = 0
    n = len(lines)
    while i < n:
        line = lines[i]
        st = line.strip()
        if st.startswith("## Milestone") and _MILESTONE_HEADER_RE.match(st):
            m = _MILESTONE_HEADER_RE.match(st)
            assert m is not None
            mid = m.group(1)
            out.append(line)
            i += 1
            start = i
            while i < n:
                t = lines[i].strip()
                if t.startswith("## Milestone") and _MILESTONE_HEADER_RE.match(t):
                    break
                i += 1
            section = lines[start:i]
            fixed, w = _repair_single_milestone_section(section, mid)
            warnings.extend(w)
            out.extend(fixed)
        else:
            out.append(line)
            i += 1
    repaired = "\n".join(out)
    if md.endswith("\n"):
        repaired += "\n"
    return repaired, warnings


def finalize_llm_milestones_md(
    raw_md: str,
    *,
    source_context: str | None = None,
    failure_artifact_dir: Path | None = None,
) -> tuple[str, list[str]]:
    """
    Normalize headers, repair common LLM omissions, parse, and validate product quality.

    Raises :class:`WeakMilestonePlanError` when markdown parses but the plan is bootstrap-only
    or not grounded in ``source_context`` (idea or vision text).

    Returns ``(milestones_md_to_persist, repair_warnings)``.

    On parse or weak-plan failure, when ``failure_artifact_dir`` is set, writes
    ``milestones_md_failure_raw.txt`` (and intermediate stages when available) for debugging.
    """
    normalized = ""
    folded = ""
    repaired = ""
    try:
        normalized, norm_warnings = normalize_milestone_markdown(raw_md)
        folded, fold_warnings = _repair_multiline_write_file_actions(normalized)
        repaired, repair_warnings = repair_llm_milestones_md(folded)
        all_warnings = [*norm_warnings, *fold_warnings, *repair_warnings]
        parsed = MilestoneService.parse_milestones(repaired)
        if not parsed:
            raise ValueError("No milestones parsed from milestones_md markdown.")
    except ValueError as exc:
        if failure_artifact_dir is not None:
            _persist_milestone_md_failure_artifacts(
                failure_artifact_dir,
                raw_md=raw_md,
                normalized=normalized,
                folded=folded,
                repaired=repaired,
            )
        raise ValueError(
            f"{exc} See {MILESTONES_DOC_PATH} for the expected milestone shape; fix the "
            "markdown or re-run `forge vertical-slice` with a clearer idea/vision."
        ) from exc
    weak = weak_parsed_milestone_plan_messages(parsed, idea_context=source_context)
    if weak:
        if failure_artifact_dir is not None:
            _persist_milestone_md_failure_artifacts(
                failure_artifact_dir,
                raw_md=raw_md,
                normalized=normalized,
                folded=folded,
                repaired=repaired,
            )
        raise WeakMilestonePlanError(weak)
    return repaired, all_warnings


def canonical_milestones_md_from_llm_raw(
    raw_milestones_md: str,
    *,
    source_context: str | None,
    failure_artifact_dir: Path | None = None,
) -> tuple[str, list[str]]:
    """
    Single entry point for turning raw LLM ``milestones_md`` JSON into the markdown that
    must be stored on disk.

    **Contract:** :func:`generate_bundle_from_llm` / :func:`generate_bundle_from_llm_fixed_vision`
    must assign the returned string to :attr:`VerticalSliceBundle.milestones_md` only—never
    ``data[\"milestones_md\"]`` from the LLM payload. :func:`materialize_bundle` then writes
    that same string to ``docs/milestones.md``.
    """
    def _strip_forge_actions(md: str) -> str:
        lines = md.splitlines()
        out: list[str] = []
        skipping = False
        for line in lines:
            stripped = line.strip()
            lower = stripped.lower()

            if lower == "- **forge actions**:":
                skipping = True
                continue

            if skipping:
                if lower == "- **forge validation**:":
                    skipping = False
                    out.append(line)
                elif stripped.startswith("## Milestone"):
                    skipping = False
                    out.append(line)
                else:
                    continue
            else:
                out.append(line)

        return "\n".join(out) + ("\n" if md.endswith("\n") else "")

    repaired, warns = finalize_llm_milestones_md(
        raw_milestones_md, source_context=source_context
    )
    repaired_stripped = _strip_forge_actions(repaired)
    return repaired_stripped, warns


