def read_vision_file_text(path: Path) -> str:
    """
    Load vision text from a file. Raises FileNotFoundError or ValueError with
    a message suitable for CLI display.
    """
    if not path.exists():
        raise FileNotFoundError(f"Vision file does not exist: {path}")
    if not path.is_file():
        raise ValueError(f"Vision path is not a file: {path}")
    try:
        raw = path.read_text(encoding="utf-8")
    except OSError as exc:
        raise OSError(f"Could not read vision file {path}: {exc}") from exc
    text = raw.strip()
    if not text:
        raise ValueError(f"Vision file is empty (no non-whitespace content): {path}")
    return text


def resolve_vision_file_path(path_str: str, *, base_dir: Path) -> Path:
    """Resolve CLI --vision-file to an absolute path (absolute paths unchanged)."""
    p = Path(path_str)
    return p if p.is_absolute() else (base_dir / p).resolve()


def _review_enforcement_status(
    planner: Planner, policy: PlannerPolicy, *, save_plan: bool
) -> dict:
    enabled = bool(getattr(policy, "require_review_for_nondeterministic", False))
    # Policy ``llm`` still requires reviewed-plan workflow even when the concrete
    # plan was synthesized deterministically from task JSON (no LLM plan call).
    planner_is_nondeterministic = bool(
        planner.metadata().get("is_nondeterministic")
    ) or (policy.mode == "llm")
    compliant = (not enabled) or (not planner_is_nondeterministic) or bool(save_plan)
    return {
        "enabled": enabled,
        "required_for_plan": planner_is_nondeterministic and enabled,
        "compliant": compliant,
        "requires_save_plan": bool(enabled and planner_is_nondeterministic),
    }


def _llm_vertical_slice_bundle_loop(
    *,
    client: LLMClient,
    bundle_llm_artifact_dir: Path | None,
    source_context: str | None,
    required_keys: tuple[str, ...],
    make_prompt: Callable[[str, str], str],
    build_bundle: Callable[[dict[str, object], str], VerticalSliceBundle],
) -> VerticalSliceBundle:
    """
    Weak-plan retry (outer) × JSON parse retry (inner).

    Each LLM response is optionally written to ``bundle_llm_artifact_dir`` as
    ``llm_bundle_raw_NN.txt`` (monotonic ``NN`` across the whole loop).
    """
    weak_extra = ""
    seq = 0
    recent_paths: list[str] = []

    for _weak_attempt in range(2):
        json_strict = ""
        for json_attempt in range(2):
            prompt = make_prompt(weak_extra, json_strict)
            raw = client.generate(prompt)
            seq += 1
            apath = write_llm_bundle_raw_artifact(
                bundle_llm_artifact_dir, sequence=seq, raw=raw
            )
            if apath is not None:
                recent_paths.append(str(apath))

            try:
                data, _jtxt, _kind, _trace = parse_vertical_slice_bundle_dict(
                    raw, required_keys=required_keys
                )
            except JsonExtractFailure as exc:
                write_llm_bundle_extraction_debug_artifact(
                    bundle_llm_artifact_dir,
                    sequence=seq,
                    trace=exc.trace,
                    error_message=str(exc),
                )
                if json_attempt == 0:
                    json_strict = STRICT_JSON_RETRY_SUFFIX
                    continue
                tail = recent_paths[-2:] if len(recent_paths) >= 2 else recent_paths
                diag = {
                    "kind": "json_extract",
                    "strategies_attempted": list(exc.trace.strategies_attempted),
                    "response_length": exc.trace.response_length,
                    "response_prefix": exc.trace.response_prefix,
                }
                msg = (
                    f"{exc} "
                    f"Vertical-slice bundle JSON failed after 2 attempts (extraction + parse). "
                    f"Inspect raw model output: {'; '.join(tail)}"
                )
                raise VerticalSliceLlmJsonError(
                    msg,
                    artifact_paths=tail,
                    extraction_diagnostics=diag,
                ) from exc
            except ValueError as exc:
                write_llm_bundle_extraction_debug_artifact(
                    bundle_llm_artifact_dir,
                    sequence=seq,
                    trace=None,
                    error_message=str(exc),
                )
                if json_attempt == 0:
                    json_strict = STRICT_JSON_RETRY_SUFFIX
                    continue
                tail = recent_paths[-2:] if len(recent_paths) >= 2 else recent_paths
                msg = (
                    f"{exc} "
                    f"Vertical-slice bundle JSON failed after 2 attempts (extraction + parse). "
                    f"Inspect raw model output: {'; '.join(tail)}"
                )
                raise VerticalSliceLlmJsonError(msg, artifact_paths=tail) from exc

            json_strict = ""
            try:
                canonical_milestones_md, _mw = canonical_milestones_md_from_llm_raw(
                    str(data["milestones_md"]),
                    source_context=source_context,
                    failure_artifact_dir=bundle_llm_artifact_dir,
                )
            except WeakMilestonePlanError as exc:
                if _weak_attempt == 0:
                    weak_extra = _milestone_regeneration_suffix(exc.messages)
                    break
                raise
            return build_bundle(data, canonical_milestones_md)
    raise RuntimeError("_llm_vertical_slice_bundle_loop: exhausted without return")


def generate_bundle_from_llm(
    idea: str,
    client: LLMClient,
    *,
    bundle_llm_artifact_dir: Path | None = None,
) -> VerticalSliceBundle:
    return _llm_vertical_slice_bundle_loop(
        client=client,
        bundle_llm_artifact_dir=bundle_llm_artifact_dir,
        source_context=idea,
        required_keys=(
            "vision",
            "requirements_md",
            "architecture_md",
            "milestones_md",
        ),
        make_prompt=lambda weak, jstrict: _build_idea_prompt(idea) + weak + jstrict,
        build_bundle=lambda data, canon: VerticalSliceBundle(
            vision=str(data["vision"]),
            requirements_md=str(data["requirements_md"]),
            architecture_md=str(data["architecture_md"]),
            milestones_md=canon,
        ),
    )


def generate_bundle_from_llm_fixed_vision(
    vision_text: str,
    client: LLMClient,
    *,
    bundle_llm_artifact_dir: Path | None = None,
) -> VerticalSliceBundle:
    """
    LLM generates requirements, architecture, and milestones only; ``vision_text`` is
    the source of truth and is not regenerated by the model.
    """
    ctx = vision_text.strip()
    return _llm_vertical_slice_bundle_loop(
        client=client,
        bundle_llm_artifact_dir=bundle_llm_artifact_dir,
        source_context=ctx,
        required_keys=("requirements_md", "architecture_md", "milestones_md"),
        make_prompt=lambda weak, jstrict: _build_fixed_vision_prompt(vision_text)
        + weak
        + jstrict,
        build_bundle=lambda data, canon: VerticalSliceBundle(
            vision=ctx,
            requirements_md=str(data["requirements_md"]),
            architecture_md=str(data["architecture_md"]),
            milestones_md=canon,
        ),
    )


def resolve_docs_llm_client() -> tuple[LLMClient | None, str | None]:
    policy, err = load_planner_policy()
    if err:
        return None, err
    if not policy.llm_client:
        return None, (
            "Vertical slice LLM docs (--idea, --vision-file, --from-vision) require "
            "planner.llm_client in forge-policy.json "
            "(e.g. 'openai' or 'anthropic' with API key, or 'stub' for offline testing)."
        )
    return resolve_llm_client_from_policy(policy)


def _failure_reason_from_apply(apply_res: dict) -> str:
    msg = apply_res.get("message")
    if msg:
        return str(msg)
    if not apply_res.get("apply_ok", True):
        errs = apply_res.get("errors") or []
        return "; ".join(str(e) for e in errs) or "apply failed"
    return str(apply_res.get("gate_summary") or "validation failed")


def _is_timeout_error(exc: Exception) -> bool:
    if isinstance(exc, TimeoutError):
        return True
    msg = str(exc).lower()
    return ("timed out" in msg) or ("timeout" in msg)


def run_vertical_slice(
    *,
    demo: bool,
    idea: str | None,
    fixed_vision: str | None = None,
    milestone_id: int,
    planner_mode: str | None,
    gate_validate: bool | None,
    gate_test_cmd: str | None,
    disable_gate_test_cmd: bool,
    gate_test_timeout_seconds: int | None,
    gate_test_output_max_chars: int | None,
    stop_after_task_prep: bool = False,
    event_bus: object | None = None,
    llm_bundle_artifact_dir: Path | None = None,
) -> dict:
    """
    Run materialize → task prep → (optional) save reviewed plan → apply with gates.
    Returns a dict with ok, stages (list), plan_id, etc.

    When ``demo`` is False, provide exactly one of ``idea`` (CLI ``--idea``) or
    ``fixed_vision`` (file-driven vision; vision text is not LLM-regenerated).
    """
    bus = as_emitter(event_bus)
    stages: list[dict] = []

    if demo:
        input_mode = "demo"
    elif fixed_vision is not None:
        input_mode = "vision_file"
    else:
        input_mode = "idea"

    bus.emit(
        RUN_STARTED,
        command="vertical-slice",
        demo=demo,
        has_idea=idea is not None,
        fixed_vision=bool(fixed_vision),
        input_mode=input_mode,
        milestone_id=milestone_id,
    )

    try:
        if demo:
            bus.emit(
                PHASE_STARTED,
                phase="materialize_docs",
                label="write vision, requirements, architecture, milestones (demo)",
            )
            bundle = demo_bundle()
            materialize_bundle(
                bundle, event_bus=bus, failure_artifact_dir=llm_bundle_artifact_dir
            )
            stages.append(
                {
                    "stage": "materialize_docs",
                    "ok": True,
                    "mode": "demo",
                    "message": "Wrote vision, requirements, architecture, milestones (demo bundle).",
                }
            )
            bus.emit(PHASE_COMPLETED, phase="materialize_docs", ok=True)
        else:
            if idea is not None and fixed_vision is not None:
                raise AssertionError(
                    "run_vertical_slice: idea and fixed_vision are mutually exclusive"
                )
            if idea is None and fixed_vision is None:
                raise AssertionError(
                    "run_vertical_slice: need idea or fixed_vision when demo is False"
                )
            doc_mode = "vision_file" if fixed_vision is not None else "idea"
            client, err = resolve_docs_llm_client()
            if err or client is None:
                msg = err or "No LLM client available."
                stages.append(
                    {
                        "stage": "materialize_docs",
                        "ok": False,
                        "mode": doc_mode,
                        "message": msg,
                    }
                )
                bus.emit(RUN_FAILED, reason=msg, phase="materialize_docs")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            gen_label = (
                "generate requirements, architecture, milestones from vision file"
                if fixed_vision is not None
                else "generate specs and milestones from idea"
            )
            bus.emit(
                PHASE_STARTED,
                phase="llm_generation",
                label=gen_label,
            )
            try:
                if fixed_vision is not None:
                    bundle = generate_bundle_from_llm_fixed_vision(
                        fixed_vision,
                        client,
                        bundle_llm_artifact_dir=llm_bundle_artifact_dir,
                    )
                else:
                    bundle = generate_bundle_from_llm(
                        idea,
                        client,
                        bundle_llm_artifact_dir=llm_bundle_artifact_dir,
                    )
            except VerticalSliceLlmJsonError as exc:
                msg = str(exc)
                paths = list(exc.artifact_paths)
                stage_row: dict = {
                    "stage": "llm_generation",
                    "ok": False,
                    "mode": doc_mode,
                    "message": msg,
                    "llm_bundle_raw_paths": paths,
                    "json_parse_failed": True,
                    "json_attempts": 2,
                }
                if exc.extraction_diagnostics:
                    stage_row["json_extraction_diagnostics"] = exc.extraction_diagnostics
                stages.append(stage_row)
                emit_data = {
                    "llm_bundle_raw_paths": paths,
                    "json_parse_failed": True,
                    "json_attempts": 2,
                }
                if exc.extraction_diagnostics:
                    emit_data["json_extraction_diagnostics"] = exc.extraction_diagnostics
                bus.emit(
                    PHASE_COMPLETED,
                    phase="llm_generation",
                    ok=False,
                    message=msg,
                    **emit_data,
                )
                bus.emit(RUN_FAILED, reason=msg, phase="llm_generation")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            except WeakMilestonePlanError as exc:
                msg = (
                    "Weak milestone plan (bootstrap-only, not grounded in the idea/vision, "
                    "or missing real code actions): "
                    + "; ".join(exc.messages)
                )
                stages.append(
                    {
                        "stage": "llm_generation",
                        "ok": False,
                        "mode": doc_mode,
                        "message": msg,
                    }
                )
                bus.emit(PHASE_COMPLETED, phase="llm_generation", ok=False, message=msg)
                bus.emit(RUN_FAILED, reason=msg, phase="llm_generation")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            except ValueError as exc:
                stages.append(
                    {
                        "stage": "llm_generation",
                        "ok": False,
                        "mode": doc_mode,
                        "message": str(exc),
                    }
                )
                bus.emit(PHASE_COMPLETED, phase="llm_generation", ok=False, message=str(exc))
                bus.emit(RUN_FAILED, reason=str(exc), phase="llm_generation")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            except Exception as exc:  # noqa: BLE001
                timeout = _is_timeout_error(exc)
                if timeout:
                    msg = (
                        "LLM generation timed out while generating "
                        "requirements/architecture/milestones."
                    )
                    failure_type = "llm_timeout"
                else:
                    msg = f"LLM generation failed: {exc}"
                    failure_type = "transient_generation_failure"
                stages.append(
                    {
                        "stage": "llm_generation",
                        "ok": False,
                        "mode": doc_mode,
                        "message": msg,
                        "failure_type": failure_type,
                        "error_detail": str(exc),
                    }
                )
                bus.emit(
                    PHASE_COMPLETED,
                    phase="llm_generation",
                    ok=False,
                    message=msg,
                    failure_type=failure_type,
                    error_detail=str(exc),
                )
                bus.emit(RUN_FAILED, reason=msg, phase="llm_generation")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            bus.emit(PHASE_COMPLETED, phase="llm_generation", ok=True)
            bus.emit(
                PHASE_STARTED,
                phase="materialize_docs",
                label="write vision, requirements, architecture, milestones",
            )
            try:
                materialize_bundle(
                    bundle, event_bus=bus, failure_artifact_dir=llm_bundle_artifact_dir
                )
            except OSError as exc:
                msg = f"Failed to write docs: {exc}"
                stages.append(
                    {
                        "stage": "materialize_docs",
                        "ok": False,
                        "mode": doc_mode,
                        "message": msg,
                    }
                )
                bus.emit(PHASE_COMPLETED, phase="materialize_docs", ok=False, message=msg)
                bus.emit(RUN_FAILED, reason=msg, phase="materialize_docs")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            except ValueError as exc:
                msg = str(exc)
                stages.append(
                    {
                        "stage": "materialize_docs",
                        "ok": False,
                        "mode": doc_mode,
                        "message": msg,
                    }
                )
                bus.emit(PHASE_COMPLETED, phase="materialize_docs", ok=False, message=msg)
                bus.emit(RUN_FAILED, reason=msg, phase="materialize_docs")
                bus.emit(RUN_COMPLETED, ok=False)
                return _finalize(stages, ok=False)
            wrote_msg = (
                "Wrote vision (from file), requirements, architecture, milestones (LLM)."
                if doc_mode == "vision_file"
                else "Wrote vision, requirements, architecture, milestones (LLM)."
            )
            stages.append(
                {
                    "stage": "materialize_docs",
                    "ok": True,
                    "mode": doc_mode,
                    "message": wrote_msg,
                }
            )
            bus.emit(PHASE_COMPLETED, phase="materialize_docs", ok=True)
    except Exception as exc:  # noqa: BLE001
        stages.append(
            {
                "stage": "materialize_docs",
                "ok": False,
                "message": str(exc),
            }
        )
        bus.emit(RUN_FAILED, reason=str(exc), phase="materialize_docs")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    policy_base, policy_err = load_planner_policy()
    if policy_err:
        msg = policy_err
        stages.append({"stage": "preview_save_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    eff_planner_policy = merge_planner_policy(policy_base, mode_override=planner_mode)

    bus.emit(
        PHASE_STARTED,
        phase="plan",
        label="ensure tasks, preview next task, save reviewed plan",
    )
    expand = ensure_tasks_for_milestone(milestone_id)
    if not expand.get("ok"):
        stages.append({"stage": "task_expand", **expand})
        bus.emit(RUN_FAILED, reason=expand.get("message", "task expand failed"), phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)
    nt = get_next_task(milestone_id)
    if nt is None:
        msg = f"No pending tasks for milestone {milestone_id}."
        stages.append({"stage": "preview_save_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    if stop_after_task_prep:
        stages.append(
            {
                "stage": "task_prepare",
                "ok": True,
                "milestone_id": milestone_id,
                "task_id": nt.id,
                "message": (
                    f"Prepared tasks for milestone {milestone_id}; stopping before reviewed plan "
                    "save/apply for pivot build flow."
                ),
            }
        )
        bus.emit(
            PHASE_COMPLETED,
            phase="plan",
            ok=True,
            message="Task preparation complete (stopped before apply).",
        )
        bus.emit(RUN_COMPLETED, ok=True)
        return _finalize(stages, ok=True)

    task_ir = compile_task_to_ir(nt)
    force_llm_for_impl = (
        task_ir.task_type in {"behavioral", "structural"} and not task_ir.has_embedded_actions
    )

    plan_mode = "llm" if force_llm_for_impl else planner_mode

    plan_planner, _, plan_resolve_err = resolve_planner(mode_override=plan_mode)
    if plan_resolve_err or plan_planner is None:
        msg = plan_resolve_err or "Planner unavailable."
        stages.append({"stage": "preview_save_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    apply_planner, _, apply_resolve_err = resolve_planner(mode_override=plan_mode)
    if apply_resolve_err or apply_planner is None:
        apply_planner = plan_planner

    enforcement = _review_enforcement_status(
        apply_planner, eff_planner_policy, save_plan=True
    )
    preview = Executor.save_reviewed_plan_for_task(
        milestone_id,
        nt.id,
        planner=plan_planner,
        planner_mode_override=planner_mode,
        review_enforcement=enforcement,
        event_bus=bus,
    )
    stages.append({"stage": "preview_save_plan", **preview})
    bus.emit(
        PHASE_COMPLETED,
        phase="plan",
        ok=bool(preview.get("ok")),
        message=preview.get("message"),
    )
    if not preview.get("ok"):
        reason = str(preview.get("message") or "plan preview or save failed")
        bus.emit(RUN_FAILED, reason=reason, phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    plan_id = preview.get("plan_id")
    if not plan_id:
        msg = "No plan_id from preview/save step."
        stages.append({"stage": "apply_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    base_policy, policy_err = load_reviewed_apply_policy()
    if policy_err:
        stages.append({"stage": "apply_plan", "ok": False, "message": policy_err})
        bus.emit(RUN_FAILED, reason=policy_err, phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    gv = gate_validate if gate_validate is not None else True
    cmd_override = gate_test_cmd
    if not disable_gate_test_cmd and cmd_override is None and demo:
        cmd_override = "python src/todo_cli.py"

    resolved: ReviewedApplyPolicy = merge_reviewed_apply_policy(
        base_policy,
        gate_validate=gv,
        test_command=cmd_override,
        disable_test_command=disable_gate_test_cmd,
        test_timeout_seconds=gate_test_timeout_seconds,
        test_output_max_chars=gate_test_output_max_chars,
    )
    if resolved.test_timeout_seconds <= 0 or resolved.test_output_max_chars <= 0:
        msg = "Invalid gate timeout or output max chars."
        stages.append({"stage": "apply_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    task_exec_policy, task_exec_err = load_task_execution_policy()
    if task_exec_err:
        stages.append({"stage": "apply_plan", "ok": False, "message": task_exec_err})
        bus.emit(RUN_FAILED, reason=task_exec_err, phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    scope = Executor.task_ids_for_reviewed_plan(plan_id)
    if scope is None:
        msg = f"Reviewed plan '{plan_id}' not found."
        stages.append({"stage": "apply_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)
    vm_id, vt_id = scope
    try:
        vm_milestone = MilestoneService.get_milestone(vm_id)
    except ValueError as exc:
        stages.append({"stage": "apply_plan", "ok": False, "message": str(exc)})
        bus.emit(RUN_FAILED, reason=str(exc), phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)
    if not vm_milestone:
        msg = "Milestone for reviewed plan no longer exists."
        stages.append({"stage": "apply_plan", "ok": False, "message": msg})
        bus.emit(RUN_FAILED, reason=msg, phase="apply_plan")
        bus.emit(RUN_COMPLETED, ok=False)
        return _finalize(stages, ok=False)

    bus.emit(PHASE_STARTED, phase="apply", label="apply reviewed plan (repair loop)")
    apply_res = Executor.run_task_apply_with_repair_loop(
        vm_id,
        vt_id,
        vm_milestone,
        planner=apply_planner,
        apply_policy=resolved,
        task_exec_policy=task_exec_policy,
        run_milestone_validation=resolved.run_validation_gate,
        initial_plan_id=plan_id,
        review_enforcement=enforcement,
        event_bus=bus,
        finalize_milestone_state_on_failure=False,
        milestone_state=None,
        state=None,
        state_file=None,
    )
    bus.emit(
        PHASE_COMPLETED,
        phase="apply",
        ok=bool(apply_res.get("ok")),
        message=apply_res.get("message"),
    )
    stages.append({"stage": "apply_plan", **apply_res})
    ok = bool(apply_res.get("ok"))
    if not ok:
        bus.emit(
            RUN_FAILED,
            reason=_failure_reason_from_apply(apply_res),
            phase="apply_or_validation",
        )
    bus.emit(RUN_COMPLETED, ok=ok)
    return _finalize(stages, ok=ok, plan_id=plan_id)


def _finalize(
    stages: list[dict], *, ok: bool, plan_id: str | None = None
) -> dict:
    return {
        "command": "vertical-slice",
        "ok": ok,
        "plan_id": plan_id or next(
            (s.get("plan_id") for s in stages if s.get("plan_id")), None
        ),
        "stages": stages,
    }
